import csv
import json
from pathlib import Path

def generate_final_report(result_dir: Path, raw_csv_path: Path, env_info: dict, args, engine=None):
    """
    Parses the raw CSV generated by sgl.bench_serving wrapper and merges it with 
    environment info and arguments to create a final comprehensive CSV for Excel.
    """
    
    final_csv_path = result_dir / f"final_report_{result_dir.name}.csv"
    
    # 1. Setup the final header
    final_headers = [
        "gpu_name", "gpu_num", "input", "output", "concurrency", "request_rate",
        "prefill_num", "decode_num", "total_throughput", "input_throughput", "output_throughput",
        "qps", "tps", "prefill_throughput", "decode_throughput",
        "TTFT (ms) mean", "TTFT (ms) median", "TTFT (ms) p99", "TTFT (ms) p95",
        "TPOT (ms) mean", "TPOT (ms) median", "TPOT (ms) p99", "TPOT (ms) p95",
        "ITL (ms) mean", "ITL (ms) median", "ITL (ms) p99", "ITL (ms) p95",
        "E2EL (ms) mean", "E2EL (ms) median", "E2EL (ms) p99", "E2EL (ms) p95",
        "source", "tp", "pp", "dp", "ep", "Reqs (num_of_prompts)", "model_name",
        "data_type", "driver", "backend", "engine", "serving framework", "cpu",
        "HW/SW Information", "EnvVar", "Cmd"
    ]
    
    # 2. Gather Environment Variables safely
    hw_info = env_info.get("hardware", {})
    sw_info = env_info.get("software", {})
    vendor_details = hw_info.get("details", {})
    
    # Extract GPUs
    gpus = vendor_details.get("gpus", ["Unknown GPU"])
    gpu_name = gpus[0].split(",")[0].strip() if gpus else "Unknown GPU"
    gpu_num = len(gpus) if gpus else 1
    driver = vendor_details.get("driver_version", "Unknown")
    
    # Missing / TODO placeholders
    cpu = env_info.get("cpu_model", env_info.get("architecture", "Unknown CPU"))
    backend = sw_info.get("backend", "Unknown Backend")
    engine = "sgl" # Currently strictly SGL
    serving_framework = f"sglang v{sw_info.get('sglang', 'unknown')}"
    data_type = getattr(args, "data_type", "")

    model_path = getattr(args, "model", "")
    if not data_type and model_path:
        config_file = Path(model_path) / "config.json"
        if config_file.exists():
            try:
                with open(config_file, "r") as cf:
                    cfg = json.load(cf)
                    data_type = cfg.get("torch_dtype", cfg.get("dtype", ""))
            except Exception:
                pass

    prefill_num = getattr(args, "prefill_num", "")
    decode_num = getattr(args, "decode_num", "")
    
    import re
    import os
    env_keys_to_capture = ["VLLM_ATTENTION_BACKEND", "CUDA_VISIBLE_DEVICES", "NCCL_P2P_DISABLE"]
    captured_envs = {k: os.environ.get(k) for k in env_keys_to_capture if k in os.environ}
    EnvVar = json.dumps(captured_envs) if captured_envs else ""
    
    Cmd = engine.server_cmd if engine and hasattr(engine, "server_cmd") else ""
    
    pp = getattr(args, "pp", "")
    if Cmd:
        m_pp = re.search(r'--(?:pp|pipeline-parallel-size)\s+(\d+)', Cmd)
        if m_pp: pp = m_pp.group(1)
        
    dp = getattr(args, "dp", "")
    if Cmd:
        m_dp = re.search(r'--(?:dp|data-parallel-size)\s+(\d+)', Cmd)
        if m_dp: dp = m_dp.group(1)
        
    ep = getattr(args, "ep", "")
    if Cmd:
        m_ep = re.search(r'--(?:ep|expert-parallel-size)\s+(\d+)', Cmd)
        if m_ep: ep = m_ep.group(1)

    source = "in-house"
    
    # JSON strings
    hw_sw_json = json.dumps(env_info)
    
    # 3. Read Raw CSV and Map Rows
    try:
        with open(raw_csv_path, "r", encoding="utf-8") as f_in, open(final_csv_path, "w", encoding="utf-8", newline='') as f_out:
            reader = csv.DictReader(f_in)
            writer = csv.writer(f_out)
            
            # Write Header
            writer.writerow(final_headers)
            
            has_req_rate_bug = "request_rate" not in reader.fieldnames
            
            for idx, row in enumerate(reader, start=1):
                if has_req_rate_bug and None in row:
                    # Restore the 1-column shift bug for older broken CSVs
                    values = list(row.values())[:-1] + row[None]
                    # We know there are 28 values total
                    row = dict(zip(
                        ["input_len", "output_len", "max_concurrency", "request_rate", "num_prompts", "Successful_requests",
                         "Benchmark_duration_s", "Total_input_tokens", "Total_generated_tokens", "Request_throughput_req_s",
                         "Output_token_throughput_tok_s", "Total_Token_throughput_tok_s", "Mean_TTFT_ms", "Median_TTFT_ms",
                         "P95_TTFT_ms", "P99_TTFT_ms", "Mean_TPOT_ms", "Median_TPOT_ms", "P95_TPOT_ms", "P99_TPOT_ms",
                         "Mean_ITL_ms", "Median_ITL_ms", "P95_ITL_ms", "P99_ITL_ms", "Mean_E2EL_ms", "Median_E2EL_ms",
                         "P95_E2EL_ms", "P99_E2EL_ms"], values
                    ))

                # We need to map the dict fields to our final headers
                # 'input_len', 'output_len', 'max_concurrency', "request_rate", 'num_prompts' ...
                
                input_len = row.get("input_len", "")
                output_len = row.get("output_len", "")
                concurrency = row.get("max_concurrency", "")
                request_rate = row.get("request_rate", "")
                num_prompts = row.get("num_prompts", "")
                
                total_throughput = row.get("Total_Token_throughput_tok_s", "")
                output_throughput = row.get("Output_token_throughput_tok_s", "")
                qps = row.get("Request_throughput_req_s", "")
                
                # Math computations:
                # input_throughput = Total Input Tokens / Duration
                input_throughput = ""
                total_input = row.get("Total_input_tokens", "")
                duration = row.get("Benchmark_duration_s", "")
                if total_input and duration and float(duration) > 0:
                    try:
                        input_throughput = f"{float(total_input) / float(duration):.2f}"
                    except ValueError:
                        pass
                
                tps = ""
                mean_tpot = row.get("Mean_TPOT_ms", "")
                if mean_tpot and float(mean_tpot) > 0:
                    try:
                        tps = f"{1000.0 / float(mean_tpot):.2f}"
                    except ValueError:
                        pass
                
                prefill_tp = ""
                if input_throughput:
                    try:
                        p_num = float(prefill_num) if prefill_num else float(gpu_num)
                        if p_num > 0:
                            prefill_tp = f"{float(input_throughput) / p_num:.2f}"
                    except ValueError:
                        pass
                
                decode_tp = ""
                if output_throughput:
                    try:
                        d_num = float(decode_num) if decode_num else float(gpu_num)
                        if d_num > 0:
                            decode_tp = f"{float(output_throughput) / d_num:.2f}"
                    except ValueError:
                        pass

                final_row = [
                    gpu_name,               # gpu_name
                    gpu_num,                # gpu_num
                    input_len,              # input
                    output_len,             # output
                    concurrency,            # concurrency
                    request_rate,           # request_rate
                    prefill_num if prefill_num else "",
                    decode_num if decode_num else "",
                    total_throughput,       # total_throughput
                    input_throughput,       # input_throughput
                    output_throughput,      # output_throughput
                    qps,                    # qps (real request rate)
                    tps,                    # tps (1000/mean_tpot)
                    prefill_tp,             # prefill_throughput
                    decode_tp,              # decode_throughput
                    row.get("Mean_TTFT_ms", ""),   
                    row.get("Median_TTFT_ms", ""),
                    row.get("P99_TTFT_ms", ""),
                    row.get("P95_TTFT_ms", ""),
                    row.get("Mean_TPOT_ms", ""),
                    row.get("Median_TPOT_ms", ""),
                    row.get("P99_TPOT_ms", ""),
                    row.get("P95_TPOT_ms", ""),
                    row.get("Mean_ITL_ms", ""),
                    row.get("Median_ITL_ms", ""),
                    row.get("P99_ITL_ms", ""),
                    row.get("P95_ITL_ms", ""),
                    row.get("Mean_E2EL_ms", ""),
                    row.get("Median_E2EL_ms", ""),
                    row.get("P99_E2EL_ms", ""),
                    row.get("P95_E2EL_ms", ""),
                    source,
                    getattr(args, "tp", "1"),
                    pp, dp, ep,
                    num_prompts,
                    Path(getattr(args, "model", "Unknown")).name,
                    data_type,
                    driver,
                    backend,
                    engine,
                    serving_framework,
                    cpu,
                    hw_sw_json,
                    EnvVar,
                    Cmd
                ]
                
                writer.writerow(final_row)
        
        pass # print(f"    ✨ Updated Final Report: {final_csv_path}")
        return final_csv_path
        
    except Exception as e:
        print(f"❌ Error generating final report: {e}")
        return None
