#!/usr/bin/env python3
"""Convert SGL benchmark CSV results into unified dashboard format."""

import argparse
import os
from datetime import datetime, timezone

import pandas as pd


TARGET_COLS = [
    "model", "gpu", "gpuNum", "tp", "dp", "pp", "ep", "batch",
    "mean_input_tokens", "mean_output_tokens", "num_concurrent_requests",
    "results_inter_token_latency_s_mean", "results_ttft_s_mean", "results_tpot_s_mean",
    "results_end_to_end_latency_s_mean",
    "results_request_output_throughput_token_per_s_mean",
    "results_mean_output_throughput_token_per_s",
    "results_num_completed_requests",
    "dataType", "driver", "driverVersion", "backend", "backendVersion",
    "engine", "engineVersion", "serving", "servingVersion", "timestamp", "source"
]


def transform_row(row: pd.Series, args: argparse.Namespace) -> dict:
    """Transform a single CSV row to the dashboard schema."""
    out: dict[str, object] = {}

    # Metadata sourced from CLI arguments
    out["model"] = args.model_alias
    out["gpu"] = args.gpu or ""
    out["gpuNum"] = args.gpu_num if args.gpu_num is not None else ""
    out["tp"] = args.tp if args.tp is not None else ""
    out["dp"] = args.dp if args.dp is not None else ""
    out["pp"] = args.pp if args.pp is not None else ""
    out["ep"] = args.ep if args.ep is not None else ""

    # Direct mapping from the CSV inputs
    max_concurrency = row.get("max_concurrency")
    out["mean_input_tokens"] = row.get("input_len")
    out["mean_output_tokens"] = row.get("output_len")
    out["batch"] = int(max_concurrency) if pd.notna(max_concurrency) else ""
    out["num_concurrent_requests"] = int(max_concurrency) if pd.notna(max_concurrency) else ""

    # Latency conversion from ms -> s (use NaN when unavailable)
    itl_ms = row.get("Mean_ITL_ms")
    ttft_ms = row.get("Mean_TTFT_ms")
    tpot_ms = row.get("Mean_TPOT_ms")
    e2e_ms = row.get("Mean_E2EL_ms")

    out["results_inter_token_latency_s_mean"] = (itl_ms / 1000.0) if pd.notna(itl_ms) else float("nan")
    out["results_ttft_s_mean"] = (ttft_ms / 1000.0) if pd.notna(ttft_ms) else float("nan")
    out["results_tpot_s_mean"] = (tpot_ms / 1000.0) if pd.notna(tpot_ms) else float("nan")
    out["results_end_to_end_latency_s_mean"] = (e2e_ms / 1000.0) if pd.notna(e2e_ms) else float("nan")

    # Throughput metrics
    out_tok_throughput = row.get("Output_token_throughput_tok_s")
    if pd.notna(out_tok_throughput):
        out["results_mean_output_throughput_token_per_s"] = out_tok_throughput
        if pd.notna(max_concurrency) and max_concurrency != 0:
            out["results_request_output_throughput_token_per_s_mean"] = out_tok_throughput / max_concurrency
        else:
            out["results_request_output_throughput_token_per_s_mean"] = float("nan")
    else:
        out["results_mean_output_throughput_token_per_s"] = float("nan")
        out["results_request_output_throughput_token_per_s_mean"] = float("nan")

    successful_requests = row.get("Successful_requests")
    out["results_num_completed_requests"] = int(successful_requests) if pd.notna(successful_requests) else ""

    # Fill with metadata
    out["dataType"] = args.data_type
    out["driver"] = args.driver
    out["driverVersion"] = args.driver_version
    out["backend"] = args.backend
    out["backendVersion"] = args.backend_version
    out["engine"] = args.engine
    out["engineVersion"] = args.engine_version or ""
    out["serving"] = args.serving or ""
    out["servingVersion"] = args.serving_version or ""
    out["timestamp"] = args.timestamp or datetime.now(timezone.utc).isoformat()
    out["source"] = args.source

    return out


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Convert SGL benchmark CSV into dashboard-compatible format")

    parser.add_argument("--model-alias", required=True, help="Uppercase model alias")
    parser.add_argument("--input", required=True, help="Path to input CSV file generated by sgl_perf.sh")

    parser.add_argument("--tp", required=True, help="Tensor parallelism", default=1)
    parser.add_argument("--dp", help="Data parallelism", default="")
    parser.add_argument("--pp", help="Pipeline parallelism", default="")
    parser.add_argument("--ep", help="Expert parallelism", default="")
    parser.add_argument("--data-type", required=True, help="Precision type (e.g., FP16)")
    parser.add_argument("--gpu", required=True, help="GPU model (e.g., A100)")
    parser.add_argument("--gpu-num", type=int, required=True, help="Number of GPUs", default=1)
    parser.add_argument("--driver", required=True, help="Driver model (e.g., NVIDIA-Linux-x86_64)")
    parser.add_argument("--driver-version", required=True, help="Driver version (e.g., 535.161.08)")
    parser.add_argument("--backend", required=True, help="Backend name (e.g., sglang)")
    parser.add_argument("--backend-version", required=True, help="Backend version")
    parser.add_argument("--engine", required=True, help="Engine name (e.g., cuda)")
    parser.add_argument("--engine-version", required=True, help="Engine version")
    parser.add_argument("--serving", required=True, help="Serving identifier (e.g., sglang)")
    parser.add_argument("--serving-version", required=True, help="Serving version")
    parser.add_argument("--source", required=True, help="Source tag for downstream tracking")
    parser.add_argument(
        "--timestamp",
        default=datetime.now(timezone.utc).isoformat(),
        help="Timestamp in ISO format"
    )
    parser.add_argument("--output", help="Output CSV path (default: convert_output/<input>_converted.csv)")

    return parser.parse_args()


def main() -> None:
    args = parse_args()

    if not os.path.exists(args.input):
        print(f"Error: Input file does not exist - {args.input}")
        return

    if not args.output:
        output_dir = "convert_output"
        os.makedirs(output_dir, exist_ok=True)
        base = os.path.splitext(os.path.basename(args.input))[0]
        output_file = f"{base}_gpu{args.gpu}_tp{args.tp}_converted.csv"
        args.output = os.path.join(output_dir, output_file)

    df = pd.read_csv(args.input)

    required_cols = [
        "max_concurrency", "Successful_requests", "Total_input_tokens", "Total_generated_tokens",
        "Output_token_throughput_tok_s", "Mean_ITL_ms", "Mean_TTFT_ms", "Mean_TPOT_ms", "Mean_E2EL_ms"
    ]
    missing = [col for col in required_cols if col not in df.columns]
    if missing:
        print(f"[ERROR] input CSV missing columns: {missing}")
        return

    out_rows: list[dict] = []
    for _, row in df.iterrows():
        if pd.notna(row.get("max_concurrency")) and pd.notna(row.get("Successful_requests")):
            out_rows.append(transform_row(row, args))

    out_df = pd.DataFrame(out_rows, columns=TARGET_COLS)

    if os.path.exists(args.output):
        print(f"Warning: Output file already exists - {args.output}")
        response = input("Overwrite? (y/n): ").strip().lower()
        if response != "y":
            print("Operation canceled")
            return

    out_df.to_csv(args.output, index=False)
    print(f"Conversion complete: {args.output} (total {len(out_df)} rows)")


if __name__ == "__main__":
    main()
